# ---------------------------------------------------------------------------
# Middleware
# ---------------------------------------------------------------------------

class ContractValidationMiddleware(BaseHTTPMiddleware):
    """Validate POST/PATCH request bodies against Forge entity contracts.

    Intercepts mutating requests to entity endpoints, validates the JSON
    body against the matching contract spec, and returns 422 with
    structured error details on validation failure.

    Skip mechanisms:
    - Non-entity routes are ignored
    - Special action routes (next, complete, etc.) are skipped
    - FORGE_SKIP_CONTRACT_VALIDATION=true env var disables globally
    """

    async def dispatch(self, request: Request, call_next):
        # Global disable via env var or missing core module
        if _SKIP_ALL or validate_contract is None:
            return await call_next(request)

        # Only validate mutating methods
        if request.method not in ("POST", "PATCH", "PUT"):
            return await call_next(request)

        # Only validate JSON content
        content_type = request.headers.get("content-type", "")
        if "application/json" not in content_type:
            return await call_next(request)

        # Match route to entity + operation
        entity, operation = _match_route(request.url.path, request.method)
        if entity is None:
            return await call_next(request)

        spec = _CONTRACTS.get((entity, operation))
        if spec is None:
            return await call_next(request)

        # Read request body (with size limit to avoid memory issues)
        try:
            body_bytes = await request.body()
            if not body_bytes:
                return await call_next(request)
            if len(body_bytes) > _MAX_BODY_SIZE:
                return JSONResponse(
                    status_code=413,
                    content={"detail": "Request body too large for validation"},
                )
            data = json.loads(body_bytes)
        except (json.JSONDecodeError, UnicodeDecodeError):
            # Let FastAPI/Pydantic handle JSON parse errors
            return await call_next(request)

        # Normalize to list for validate_contract
        items = data if isinstance(data, list) else [data]

        # Run contract validation
        errors = validate_contract(spec, items)
        if errors:
            logger.warning(
                "Contract validation failed for %s %s: %s",
                request.method,
                request.url.path,
                errors,
            )
            return JSONResponse(
                status_code=422,
                content={
                    "detail": "Contract validation failed",
                    "entity": entity,
                    "operation": operation,
                    "errors": errors,
                },
            )

        return await call_next(request)
