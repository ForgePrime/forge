logger = logging.getLogger(__name__)

# Global disable via environment variable
_SKIP_ALL = os.environ.get("FORGE_SKIP_CONTRACT_VALIDATION", "").lower() == "true"

# Max request body size for contract validation (1 MB)
_MAX_BODY_SIZE = 1_000_000

# ---------------------------------------------------------------------------
# Route matching
# ---------------------------------------------------------------------------

# Match: /api/v1/projects/{slug}/{entity}
_COLLECTION_RE = re.compile(
    r"^/api/v1/projects/[^/]+/"
    r"(tasks|decisions|changes|objectives|ideas|guidelines|knowledge|lessons|ac-templates|gates)$"
)

# Match: /api/v1/projects/{slug}/{entity}/{id}
_ITEM_RE = re.compile(
    r"^/api/v1/projects/[^/]+/"
    r"(tasks|decisions|objectives|ideas|guidelines|knowledge|ac-templates)"
    r"/[^/]+$"
)

# POST routes that are NOT add/create operations — skip validation
_SKIP_SUFFIXES = (
    "/next",
    "/complete",
    "/context",
    "/commit",
    "/promote",
    "/link",
    "/unlink",
    "/instantiate",
    "/check",
)


def _should_skip(path: str) -> bool:
    """Check if this path is a special action endpoint (not add/update)."""
    if "/auth/" in path:
        return True
    return any(path.endswith(suffix) for suffix in _SKIP_SUFFIXES)


def _match_route(path: str, method: str) -> tuple[str | None, str | None]:
    """Extract (entity, operation) from request path + method.

    Returns (None, None) if the route is not subject to contract validation.
    """
    if _should_skip(path):
        return None, None

    if method == "POST":
        m = _COLLECTION_RE.match(path)
        if m:
            return m.group(1), "add"

    elif method in ("PATCH", "PUT"):
        m = _ITEM_RE.match(path)
        if m:
            return m.group(1), "update"

    return None, None


