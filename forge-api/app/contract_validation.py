"""Contract validation middleware.

Validates POST/PATCH request bodies against Forge entity contracts,
using the same validation engine as the CLI (core/contracts.py).

API-adapted specs: IDs are auto-generated in the API, so "id" is not
required in request bodies. All other structural validation (required
fields, enums, types) mirrors core module CONTRACTS definitions.

Decisions update contract intentionally diverges from core module:
the API uses direct field updates (status, recommendation, reasoning)
instead of the CLI's action-based pattern (accept, override, defer).
"""

